package aed;

interface Iterador<T> {
  /*La interfaz Iterador<T> declara los siguientes métodos:*/

  public boolean haySiguiente(); // Devuelve true si hay un elemento siguiente en la colección.
  public T siguiente();          // Devuelve el elemento siguiente en la colección y avanza el iterador.
}

